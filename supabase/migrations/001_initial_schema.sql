-- ----------------------------------------------------------------------------
-- Helpful view: installment with computed OVERDUE status
-- ----------------------------------------------------------------------------
-- We don't store 'OVERDUE' on installments — we compute it on read.
-- This view exposes effective_status which is what the UI should use.
create or replace view public.v_installments_with_effective_status as
select
  i.*,
  case
    when i.status in ('PAID','WAIVED') then i.status
    when i.due_date < current_date and i.status in ('PENDING','PARTIAL') then 'OVERDUE'
    when i.due_date = current_date and i.status in ('PENDING','PARTIAL') then 'DUE_TODAY'
    else i.status
  end as effective_status,
  (i.due_amount - i.paid_amount) as balance,
  a.account_type,
  a.customer_id,
  c.full_name as customer_name,
  c.phone_number as customer_phone
from public.installments i
join public.accounts  a on a.id = i.account_id
join public.customers c on c.id = a.customer_id;


-- ----------------------------------------------------------------------------
-- accounts
-- ----------------------------------------------------------------------------
-- One customer can have many accounts. Two flavors:
--   BC_WEEKLY: amount_given < total_repayment_amount, term_weeks set,
--              installments auto-generated on insert.
--   MONTHLY_INTEREST: principal_amount + apr set, monthly installments
--              generated by the function `generate_monthly_interest_installment`.
create table public.accounts (
  id                       uuid primary key default gen_random_uuid(),
  owner_user_id            uuid not null references auth.users(id) on delete cascade,
  customer_id              uuid not null references public.customers(id) on delete restrict,

  account_type             text not null check (account_type in ('BC_WEEKLY','MONTHLY_INTEREST')),
  status                   text not null default 'ACTIVE' check (status in ('ACTIVE','CLOSED','DEFAULTED')),

  -- BC_WEEKLY fields
  amount_given             numeric(14,2),
  total_repayment_amount   numeric(14,2),
  profit_amount            numeric(14,2) generated always as (
    coalesce(total_repayment_amount, 0) - coalesce(amount_given, 0)
  ) stored,
  term_weeks               int,

  -- MONTHLY_INTEREST fields
  principal_amount         numeric(14,2),
  apr                      numeric(6,3),  -- e.g. 24.000

  -- Common
  start_date               date not null,
  -- For BC: weekday name (MONDAY etc) OR null
  -- For monthly: day-of-month "1".."28"
  due_day                  text,
  notes                    text,

  closed_at                timestamptz,
  created_at               timestamptz not null default now(),
  updated_at               timestamptz not null default now(),

  constraint bc_weekly_fields_valid check (
    account_type <> 'BC_WEEKLY' or (
      amount_given is not null and
      total_repayment_amount is not null and
      term_weeks is not null and term_weeks > 0
    )
  ),
  constraint monthly_fields_valid check (
    account_type <> 'MONTHLY_INTEREST' or (
      principal_amount is not null and apr is not null and apr >= 0
    )
  )
);

create index idx_accounts_owner    on public.accounts(owner_user_id);
